package week13d01;

public class TownSenior {

    private String name;
    private String postCode;

    public TownSenior(String name, String postCode) {
        this.name = name;
        this.postCode = postCode;
    }

    public String getName() {
        return name;
    }

    public String getPostCode() {
        return postCode;
    }
}
